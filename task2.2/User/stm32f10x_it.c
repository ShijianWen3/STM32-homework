// 外部中断处理函数
void EXTI0_IRQHandler(void) 
{
    if (EXTI_GetITStatus(EXTI_Line0) != RESET) 
	{
        if (GPIO_ReadInputDataBit(GPIOA, GPIO_Pin_0) == RESET) 
		{
            // 按键按下
            button_state = 1;
            button_press_time = 0;  // 重置按下时间
        } 
		else 
		{
            // 按键释放
            button_state = 0;
            if (button_press_time < 1000) 
			{
                // 短按处理
                ShortPressAction();
            } 
			else 
			{
                // 长按处理
                LongPressAction();
            }
        }
        
        // 清除中断标志
        EXTI_ClearITPendingBit(EXTI_Line0);
    }
}


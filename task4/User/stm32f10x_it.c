void TIM3_IRQHandler(void)
{
    // 这里可实现定时器3中断服务程序
    // 如果需要周期性地调整占空比，可以在中断中改变 pwm_duty

    if (pwm_direction == 1) {
        if (pwm_duty < PWM_MAX_DUTY) {
            pwm_duty += 10;  // 增加占空比（亮度增加）
        } else {
            pwm_direction = -1;  // 改变方向
        }
    } else if (pwm_direction == -1) {
        if (pwm_duty > PWM_MIN_DUTY) {
            pwm_duty -= 10;  // 减少占空比（亮度减少）
        } else {
            pwm_direction = 1;  // 改变方向
        }
    }

    // 更新占空比
    TIM3->CCR2 = pwm_duty;
}